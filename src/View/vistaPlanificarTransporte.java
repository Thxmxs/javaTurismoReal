    private void btonAgregarTransporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btonAgregarTransporteActionPerformed
        Date f1 = FechaRecogida.getDate();
        if(txtLugarRecogida.getText().equals("")|| txtIdReserva.getText().equals("") || txtCostoTransporte.getText().equals("") || txtNombreChofer.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Debe rellenar todos los campos");
        }
        else{
        tm.AgregarTransporte(txtLugarRecogida, f1, txtIdReserva, txtCostoTransporte, txtNombreChofer);
        tm.cargarTablaTransportes(tablaReservas);
        }
        
    }//GEN-LAST:event_btonAgregarTransporteActionPerformed

