    private void tablaReservasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaReservasMouseClicked
        int fila = tablaReservas.getSelectedRow();
        String codigo = tablaReservas.getValueAt(fila, 0).toString();

        tm.pasarIdReservaACampo(codigo, txtIdReserva);
    }//GEN-LAST:event_tablaReservasMouseClicked

    private void tablaTransportesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaTransportesMouseClicked
        int fila = tablaTransportes.getSelectedRow();
        String codigo = tablaTransportes.getValueAt(fila, 0).toString();
        tm.pasarTransporteACampos(codigo, txtIdTransporte, txtLugarRecogida, FechaRecogida, txtIdReserva, txtCostoTransporte, txtNombreChofer);
    }//GEN-LAST:event_tablaTransportesMouseClicked

    private void btonActualizarTransporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btonActualizarTransporteActionPerformed
        Date f1 = FechaRecogida.getDate();
        if (txtIdTransporte.getText().equals("") || txtLugarRecogida.getText().equals("") || txtCostoTransporte.equals("") || txtNombreChofer.equals("")) {
            JOptionPane.showMessageDialog(null, "Debe rellenar los campos");
        } else {
            tm.ActualizarTransporte(txtIdTransporte, txtLugarRecogida, f1, txtIdReserva, txtCostoTransporte, txtNombreChofer);
            tm.cargarTablaTransportes(tablaTransportes);
        }

    }//GEN-LAST:event_btonActualizarTransporteActionPerformed

    private void btonEliminarTransporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btonEliminarTransporteActionPerformed
        if (txtIdTransporte.getText().equals("") ) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el id de transporte");
        } else {
            tm.EliminarTransporte(txtIdTransporte);
            tm.cargarTablaTransportes(tablaTransportes);
        }

    }//GEN-LAST:event_btonEliminarTransporteActionPerformed

