    private void btnCreateCheckListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateCheckListActionPerformed
        int fila = jTableDepartamentos.getSelectedRow();
        DefaultTableModel table = (DefaultTableModel) jTableDepartamentos.getModel();
        
        if (fila < 0) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar un departamento para poder crear el CheckList");   
        }
        else{
            String estado = String.valueOf(table.getValueAt(jTableDepartamentos.getSelectedRow(), 3));
            if (estado.equals("Ocupado")) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un departamento disponible o reservado");
            }
            if (estado.equals("En Mantencion")) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un departamento disponible o reservado");
            }
            else{
              String de = String.valueOf(table.getValueAt(jTableDepartamentos.getSelectedRow(), 0));
              int iddep = Integer.parseInt(de);
              
              this.dispose();
              CheckListViewSubView clvs = new CheckListViewSubView(iddep);
              clvs.setVisible(true);  
            }
            
            
        }
    }//GEN-LAST:event_btnCreateCheckListActionPerformed

