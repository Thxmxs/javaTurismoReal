    private void btnIngresarEstadoDepartamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarEstadoDepartamentoActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTableDepartamentos.getModel();
        int fila = jTableDepartamentos.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar una fila para poder ingresar el estado");
        }
        else{
            String estado = String.valueOf(model.getValueAt(jTableDepartamentos.getSelectedRow(), 3));
            if (estado.equals("Ocupado")) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un departamento disponible o reservado");
            }
            if (estado.equals("En Mantencion")) {
                JOptionPane.showMessageDialog(this, "Debes seleccionar un departamento disponible o reservado");
            }
            else{
            //guardo el id de departamento en una variable int
            String idFila = String.valueOf(model.getValueAt(jTableDepartamentos.getSelectedRow(), 0));
            int idDep = Integer.parseInt(idFila);
            h.setEstadoHabitacion(txtAestadoDepartamento.getText());
            clm.actualizarEstadoDepartamento(txtAestadoDepartamento, idDep);
            btnCreateCheckList.setEnabled(true);
            JOptionPane.showMessageDialog(this, "Exito");
            }
        }       
            
       
        
    }//GEN-LAST:event_btnIngresarEstadoDepartamentoActionPerformed

