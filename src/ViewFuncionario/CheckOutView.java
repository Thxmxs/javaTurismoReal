    private void btnIngresarCheckOutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarCheckOutActionPerformed
 DefaultTableModel model = (DefaultTableModel) JTableCheckOut.getModel();
        boolean resultado = false;
        int fila = JTableCheckOut.getSelectedRow();
        try {
            
            if (resultado == false) {
                if (fila < 0) {
                    JOptionPane.showMessageDialog(this, "Debe seleccionar una fila para poder ingresar el check in");
                    resultado = true;
                }
            }
            //guardo el texto de multas
            int multa = Integer.parseInt(txtMultas.getText());
            
            //guardo el texto de costo reparacion
            int costoReparacion = Integer.parseInt(txtCostoReparacion.getText());
          
            String f = String.valueOf(model.getValueAt(JTableCheckOut.getSelectedRow(), 3));
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            Date fecha = formato.parse(f);
            
            String r = String.valueOf(model.getValueAt(JTableCheckOut.getSelectedRow(), 1));
            int idReserva = Integer.parseInt(r);
            
            String d = String.valueOf(model.getValueAt(JTableCheckOut.getSelectedRow(), 2));
            int idDepa = Integer.parseInt(d);
            if (!txtMultas.getText().matches("[0-9]+")) {
                JOptionPane.showMessageDialog(this, "Solo se pueden ingresar numeros en el campo multas");
                resultado = true;
            }
            if (!txtCostoReparacion.getText().matches("[0-9]+")) {
                JOptionPane.showMessageDialog(this, "Solo se pueden ingresar numeros en el campo costo reparacion");
                resultado = true;
            }
            String comentarios = txtComentarios.getText();
            if (comentarios.equals("")) {
                JOptionPane.showMessageDialog(this, "El campo comentarios no puede quedar vacio");
                resultado = true;
            }
            if (resultado == false) {
                com.insertarCheckOut(multa, costoReparacion, txtComentarios, fecha, idReserva, idDepa);
                System.out.println("se ingreso el check_out");
                com.listarReservaCheckOut(JTableCheckOut);
                CheckListView clv = new CheckListView();
                clv.btnVolver.setEnabled(false);
                clv.setVisible(true);
                this.dispose();
            }
            
            
            
        } catch (Exception e) {
            System.out.println("no se ingreso checkout"+ e.getMessage());
        }
        
    }//GEN-LAST:event_btnIngresarCheckOutActionPerformed

